export const getItemName = (item, lang) => {
  if (!item) return ''
  const frName = item.name ?? item.fr ?? ''
  if (!lang || lang === 'fr') return frName
  return ITEM_TRANSLATIONS[frName]?.[lang] ?? frName
}

